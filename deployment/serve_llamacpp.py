"""llama.cpp / Ollama server launcher for GGUF models.

Serves GGUF-quantized models via llama.cpp's built-in server or
creates an Ollama Modelfile and registers the model for Ollama serving.

Usage::

    python deployment/serve_llamacpp.py --model checkpoints/gguf/model-q4_k_m.gguf \\
        --backend ollama --model-name minigpt
"""

from __future__ import annotations

import argparse
import logging
import os
import shutil
import subprocess
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# llama.cpp server
# ---------------------------------------------------------------------------

def launch_llamacpp_server(
    model_path: str,
    host: str = "0.0.0.0",
    port: int = 8080,
    n_ctx: int = 4096,
    n_gpu_layers: int = -1,
    n_threads: int | None = None,
    n_batch: int = 512,
    llama_cpp_path: str | None = None,
) -> None:
    """Launch a llama.cpp HTTP server.

    Parameters
    ----------
    model_path:
        Path to the GGUF model file.
    host:
        Server host.
    port:
        Server port.
    n_ctx:
        Context window size.
    n_gpu_layers:
        Number of layers to offload to GPU. ``-1`` for all layers.
    n_threads:
        Number of CPU threads.
    n_batch:
        Batch size for prompt evaluation.
    llama_cpp_path:
        Path to llama.cpp build directory.
    """
    server_bin = _find_llama_server(llama_cpp_path)
    if not server_bin:
        logger.error(
            "llama.cpp server binary not found. Build llama.cpp:\n"
            "  git clone https://github.com/ggerganov/llama.cpp\n"
            "  cd llama.cpp && make llama-server"
        )
        raise FileNotFoundError("llama-server not found")

    cmd = [
        server_bin,
        "--model", model_path,
        "--host", host,
        "--port", str(port),
        "--ctx-size", str(n_ctx),
        "--n-gpu-layers", str(n_gpu_layers),
        "--batch-size", str(n_batch),
    ]

    if n_threads:
        cmd.extend(["--threads", str(n_threads)])

    logger.info("Launching llama.cpp server: %s", " ".join(cmd))
    logger.info("API at http://%s:%d", host, port)

    try:
        subprocess.run(cmd, check=True)
    except KeyboardInterrupt:
        logger.info("Server shut down by user")
    except subprocess.CalledProcessError as exc:
        logger.error("llama.cpp server exited with code %d", exc.returncode)
        raise


def _find_llama_server(llama_cpp_path: str | None = None) -> str | None:
    """Locate the llama.cpp server binary."""
    search_names = ["llama-server", "server"]

    if llama_cpp_path:
        base = Path(llama_cpp_path)
        for name in search_names:
            for sub in ["", "build/bin/", "build/"]:
                candidate = base / sub / name
                if candidate.exists():
                    return str(candidate)

    # Check PATH
    for name in search_names:
        found = shutil.which(name)
        if found:
            return found

    # Check common locations
    env_path = os.environ.get("LLAMA_CPP_PATH", "")
    if env_path:
        for name in search_names:
            candidate = Path(env_path) / name
            if candidate.exists():
                return str(candidate)

    return None


